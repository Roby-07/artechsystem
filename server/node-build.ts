import path from "path";
import { createServer } from "./index";
import * as express from "express";

const app = createServer();
const port = process.env.PORT || 3000;

// In production, serve the built SPA files
const __dirname = import.meta.dirname;
const distPath = path.join(__dirname, "../spa");

// Serve static files with long-term caching for assets
app.use(
  express.static(distPath, {
    etag: true,
    maxAge: "1y",
    setHeaders: (res, filePath) => {
      // Cache immutable hashed assets aggressively
      if (/(?:\.|-)\w{8,}\.(?:js|css|png|jpg|jpeg|webp|svg|gif|ico|woff2?)$/i.test(filePath)) {
        res.setHeader("Cache-Control", "public, max-age=31536000, immutable");
      }
    },
  })
);

// Handle React Router - serve index.html for all non-API routes
app.get("*", (req, res) => {
  // Don't serve index.html for API routes
  if (req.path.startsWith("/api/") || req.path.startsWith("/health")) {
    return res.status(404).json({ error: "API endpoint not found" });
  }

  // Disable caching for the HTML shell to ensure latest deployments are loaded
  res.setHeader("Cache-Control", "no-store, no-cache, must-revalidate, proxy-revalidate");
  res.setHeader("Pragma", "no-cache");
  res.setHeader("Expires", "0");
  res.sendFile(path.join(distPath, "index.html"));
});

app.listen(port, () => {
  console.log(`ðŸš€ Fusion Starter server running on port ${port}`);
  console.log(`ðŸ“± Frontend: http://localhost:${port}`);
  console.log(`ðŸ”§ API: http://localhost:${port}/api`);
});

// Graceful shutdown
process.on("SIGTERM", () => {
  console.log("ðŸ›‘ Received SIGTERM, shutting down gracefully");
  process.exit(0);
});

process.on("SIGINT", () => {
  console.log("ðŸ›‘ Received SIGINT, shutting down gracefully");
  process.exit(0);
});
